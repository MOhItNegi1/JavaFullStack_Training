package whileloop;
public class printrange {
    public static void main(String args[]){
        // To print from the range -123 to -129
        int i = -123;
        while(i >= -129){
            System.out.println(i);
            i--;
        }
        int j=79;
        while(j<=84){
            System.out.println(j);
            j++;
        }



    }

}